#ifndef LLVM_LIB_TARGET_MyArch_MyArchSUBTARGET_H
#define LLVM_LIB_TARGET_MyArch_MyArchSUBTARGET_H

#include "MyArchFrameLowering.h"
#include "MyArchISelLowering.h"
#include "MyArchInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "MyArchGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class MyArchSubtarget : public MyArchGenSubtargetInfo {
  virtual void anchor();

  MyArchRegisterInfo RegInfo;
  MyArchInstrInfo InstrInfo;
  MyArchFrameLowering FrameLowering;
  MyArchTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  MyArchSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const MyArchInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const MyArchFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const MyArchTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const MyArchRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_MyArch_MyArchSUBTARGET_H
